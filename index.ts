///created a variable then added square brackets []////
/// In typescript square brackets[]  is an array where 
//we can store all type of elements either a alphabets,numbers 
// or anything else
let bunch_of_fruits:string[]=["apple","grapes","orange",];
///push is a function in array that will help us to add element
// in last of the array 
 bunch_of_fruits.push("mango")
console.log(bunch_of_fruits)
///unshift is a function in array that will help us to add element 
// in the begining of  array
bunch_of_fruits.unshift("bannana")
console.log(bunch_of_fruits)
///pop is a function in array that will help us to remove element
// at the last of  array
bunch_of_fruits.pop()
console.log(bunch_of_fruits.pop())

